// Enable/Disable queue
queuesRouter.post('/:mikrotikId/:queueId/toggle', async (req: AuthRequest, res: Response) => {
  try {
    const { mikrotikId, queueId } = req.params;
    const { disabled } = req.body;
    const hasAccess = await verifyDeviceAccess(req.userId!, req.userRole!, mikrotikId);
    if (!hasAccess) return res.status(403).json({ error: 'Sin acceso' });

    const config = await getDeviceConfig(pool, mikrotikId);
    const data = await mikrotikRequest(config, `/rest/queue/simple/${queueId}`, 'PATCH', {
      disabled: disabled ? 'true' : 'false',
    });
    res.json({ success: true, data });
  } catch (error: any) {
    res.status(500).json({ success: false, error: error.message });
  }
});
