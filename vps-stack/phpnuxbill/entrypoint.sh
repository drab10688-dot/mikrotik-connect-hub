#!/bin/bash
set -Eeuo pipefail

log() {
  echo "$1"
}

log "=== Configurando PHPNuxBill ==="

NUXROOT="/var/www/html"
CONFIG_FILE="$NUXROOT/config.php"

DB_HOST="${NUXBILL_DB_HOST:-mariadb}"
DB_USER="${NUXBILL_DB_USER:-nuxbill}"
DB_PASS="${NUXBILL_DB_PASS:-changeme}"
DB_NAME="${NUXBILL_DB_NAME:-phpnuxbill}"
APP_URL="${NUXBILL_APP_URL:-http://localhost:8080}"
TZ_VALUE="${TZ:-America/Bogota}"

APP_KEY=$(echo -n "${DB_PASS}${DB_HOST}" | md5sum | cut -d' ' -f1)

# 1) Detectar instalación en subdirectorio y mover al webroot
if [ ! -f "$NUXROOT/index.php" ] && [ -f "$NUXROOT/phpnuxbill/index.php" ]; then
  cp -r "$NUXROOT/phpnuxbill/"* "$NUXROOT/" 2>/dev/null || true
  cp -r "$NUXROOT/phpnuxbill/."* "$NUXROOT/" 2>/dev/null || true
  rm -rf "$NUXROOT/phpnuxbill"
  log "Archivos movidos al webroot ✓"
fi

# 2) Generar config.php
if [ -n "$DB_HOST" ]; then
  {
    echo "<?php"
    echo "// Auto-generated by OmniSync installer"
    echo "\$db_host = '${DB_HOST}';"
    echo "\$db_user = '${DB_USER}';"
    echo "\$db_pass = '${DB_PASS}';"
    echo "\$db_password = '${DB_PASS}';"
    echo "\$db_name = '${DB_NAME}';"
    echo
    echo "define('APP_URL', '${APP_URL}');"
    echo "\$APP_URL = APP_URL;"
    echo
    echo "\$_app_stage = 'Live';"
    echo "\$app_stage = \$_app_stage;"
    echo
    echo "\$APP_KEY = '${APP_KEY}';"
    echo
    echo "date_default_timezone_set('${TZ_VALUE}');"
  } > "$CONFIG_FILE"

  chown www-data:www-data "$CONFIG_FILE"
  chmod 644 "$CONFIG_FILE"
  log "PHPNuxBill config.php creado ✓"
fi

# 3) Esperar MariaDB
log "Esperando MariaDB..."
CONNECTED=false
for i in $(seq 1 30); do
  if php -r "new mysqli('${DB_HOST}', '${DB_USER}', '${DB_PASS}', '${DB_NAME}');" 2>/dev/null; then
    log "MariaDB conectada ✓"
    CONNECTED=true
    break
  fi
  log "Reintentando (${i}/30)..."
  sleep 2
done

# 4) Validar/importar schema si hace falta
SCHEMA_FOUND=false
if [ "$CONNECTED" = true ]; then
  TABLE_CHECK=$(php -r "
    \$c = new mysqli('${DB_HOST}', '${DB_USER}', '${DB_PASS}', '${DB_NAME}');
    if (\$c->connect_error) { echo '0'; exit; }
    \$r = \$c->query(\"SHOW TABLES LIKE 'tbl_appconfig'\");
    echo ((\$r && \$r->num_rows > 0) ? '1' : '0');
    \$c->close();
  " 2>/dev/null || echo "0")

  if [ "$TABLE_CHECK" = "0" ]; then
    log "Tablas no encontradas, buscando schema..."

    CANDIDATES=(
      "$NUXROOT/install/phpnuxbill.sql"
      "$NUXROOT/install/sql/phpnuxbill.sql"
      "$NUXROOT/system/uploads/phpnuxbill.sql"
      "$NUXROOT/install/database.sql"
      "$NUXROOT/install/install.sql"
      "$NUXROOT/sql/install.sql"
    )

    while IFS= read -r f; do
      [ -n "$f" ] && CANDIDATES+=("$f")
    done < <(find "$NUXROOT" -name "phpnuxbill.sql" 2>/dev/null)

    while IFS= read -r f; do
      [ -n "$f" ] && CANDIDATES+=("$f")
    done < <(find "$NUXROOT/install" -name "*.sql" 2>/dev/null | head -3)

    for sql_file in "${CANDIDATES[@]}"; do
      [ -z "$sql_file" ] && continue
      if [ -f "$sql_file" ]; then
        log "Importando schema: $sql_file"
        if php -r "
          \$c = new mysqli('${DB_HOST}', '${DB_USER}', '${DB_PASS}', '${DB_NAME}');
          if (\$c->connect_error) { exit(1); }
          \$sql = file_get_contents('${sql_file}');
          if (\$sql === false || trim(\$sql) === '') { exit(1); }
          if (!\$c->multi_query(\$sql)) { exit(1); }
          while (\$c->more_results() && \$c->next_result()) {}
          \$c->close();
        " 2>/dev/null; then
          SCHEMA_FOUND=true
          break
        fi
      fi
    done

    if [ "$SCHEMA_FOUND" = true ]; then
      log "Schema importado ✓"
    else
      log "⚠ No se encontró schema SQL. Acceda vía navegador para instalar."
      if [ -d "$NUXROOT/install" ]; then
        chown -R www-data:www-data "$NUXROOT/install"
        log "Directorio install/ disponible para instalación web"
      fi
    fi
  else
    log "PHPNuxBill schema ya existe ✓"
    SCHEMA_FOUND=true
  fi
fi

# 5) Crear admin si falta + forzar Live + configurar RADIUS DB + inicializar tablas RADIUS
if [ "$CONNECTED" = true ] && [ "$SCHEMA_FOUND" = true ]; then
  RADIUS_DB_HOST="${NUXBILL_DB_HOST:-mariadb}"
  RADIUS_DB_USER="${RADIUS_DB_USER:-radius}"
  RADIUS_DB_PASS="${RADIUS_DB_PASS:-changeme_radius}"
  RADIUS_DB_NAME="${RADIUS_DB_NAME:-radius}"

  # 5a) Asegurar que las tablas RADIUS existen en MariaDB
  RADIUS_SCHEMA="/docker-entrypoint-initdb.d/radius-schema.sql"
  if [ -f "$RADIUS_SCHEMA" ]; then
    RADIUS_TABLE_CHECK=$(php -r "
      \$c = new mysqli('${RADIUS_DB_HOST}', '${RADIUS_DB_USER}', '${RADIUS_DB_PASS}', '${RADIUS_DB_NAME}');
      if (\$c->connect_error) { echo '0'; exit; }
      \$r = \$c->query(\"SHOW TABLES LIKE 'nas'\");
      echo ((\$r && \$r->num_rows > 0) ? '1' : '0');
      \$c->close();
    " 2>/dev/null || echo "0")

    if [ "$RADIUS_TABLE_CHECK" = "0" ]; then
      log "Tablas RADIUS no encontradas, importando schema..."
      mysql -h"${RADIUS_DB_HOST}" -u"${RADIUS_DB_USER}" -p"${RADIUS_DB_PASS}" "${RADIUS_DB_NAME}" < "$RADIUS_SCHEMA" 2>/dev/null && \
        log "Schema RADIUS importado ✓" || \
        log "⚠ No se pudo importar schema RADIUS con mysql client, intentando con PHP..."

      # Fallback: importar con PHP si mysql client no está disponible
      if [ "$RADIUS_TABLE_CHECK" = "0" ]; then
        php -r "
          \$c = new mysqli('${RADIUS_DB_HOST}', '${RADIUS_DB_USER}', '${RADIUS_DB_PASS}', '${RADIUS_DB_NAME}');
          if (\$c->connect_error) { echo 'Error: '.\$c->connect_error; exit(1); }
          \$sql = file_get_contents('${RADIUS_SCHEMA}');
          \$c->multi_query(\$sql);
          do { if (\$r = \$c->store_result()) \$r->free(); } while (\$c->more_results() && \$c->next_result());
          echo 'Schema RADIUS importado via PHP ✓';
          \$c->close();
        " 2>/dev/null || log "⚠ Error importando schema RADIUS"
      fi
    else
      log "Tablas RADIUS ya existen ✓"
    fi
  else
    log "⚠ Schema RADIUS no encontrado en $RADIUS_SCHEMA"
  fi

  # 5b) Crear admin + configurar RADIUS
  php -r "
    \$c = new mysqli('${DB_HOST}', '${DB_USER}', '${DB_PASS}', '${DB_NAME}');
    if (\$c->connect_error) { exit; }

    \$r = \$c->query(\"SELECT id FROM tbl_users WHERE username='admin'\");
    if (\$r && \$r->num_rows == 0) {
      \$hash = sha1('admin');
      \$c->query(\"INSERT INTO tbl_users (username, password, fullname, user_type, status) VALUES ('admin', '\$hash', 'Administrator', 'Admin', 'Active')\");
      echo 'Admin creado (admin/admin) ✓';
    } else {
      echo 'Admin ya existe ✓';
    }

    \$c->query(\"INSERT INTO tbl_appconfig (setting, value) VALUES ('app_stage', 'Live') ON DUPLICATE KEY UPDATE value='Live'\");

    // Configurar conexión RADIUS DB para PHPNuxBill
    \$radius_settings = [
      'radius_host' => '${RADIUS_DB_HOST}',
      'radius_user' => '${RADIUS_DB_USER}',
      'radius_pass' => '${RADIUS_DB_PASS}',
      'radius_name' => '${RADIUS_DB_NAME}',
      'radius_db_host' => '${RADIUS_DB_HOST}',
      'radius_db_user' => '${RADIUS_DB_USER}',
      'radius_db_pass' => '${RADIUS_DB_PASS}',
      'radius_db_name' => '${RADIUS_DB_NAME}',
      'radius_enable' => '1',
    ];
    foreach (\$radius_settings as \$k => \$v) {
      \$ks = \$c->real_escape_string(\$k);
      \$vs = \$c->real_escape_string(\$v);
      \$c->query(\"INSERT INTO tbl_appconfig (setting, value) VALUES ('\$ks', '\$vs') ON DUPLICATE KEY UPDATE value='\$vs'\");
    }
    echo 'RADIUS DB configurada ✓';

    \$c->close();
  " 2>/dev/null || true

  if [ -d "$NUXROOT/install" ]; then
    mv "$NUXROOT/install" "$NUXROOT/install.bak" 2>/dev/null || true
    log "Directorio install/ renombrado a install.bak ✓"
  fi
fi

# 6) Permisos
chown -R www-data:www-data "$NUXROOT"
chmod -R 755 "$NUXROOT"

# 7) Configurar cron por HTTP (contexto web completo)
log "Configurando cron job..."

CRON_FILE=""
if [ -f "$NUXROOT/system/cron.php" ]; then
  CRON_FILE="$NUXROOT/system/cron.php"
  log "Cron file encontrado: $CRON_FILE"
fi

if [ "$CONNECTED" = true ] && [ "$SCHEMA_FOUND" = true ]; then
  php -r "
    \$c = new mysqli('${DB_HOST}', '${DB_USER}', '${DB_PASS}', '${DB_NAME}');
    if (!\$c->connect_error) {
      \$settings = [
        'cron_last_run' => date('Y-m-d H:i:s'),
        'cron_period' => '1',
        'cron_status' => '1',
        'cron_active' => '1',
        'disable_cron_warning' => '1',
        'cron_enabled' => '1',
        'cron_enable' => '1',
        'cron_warning' => '0',
      ];

      foreach (\$settings as \$k => \$v) {
        \$ks = \$c->real_escape_string(\$k);
        \$vs = \$c->real_escape_string(\$v);
        \$c->query(\"DELETE FROM tbl_appconfig WHERE setting='\$ks'\");
        \$c->query(\"INSERT INTO tbl_appconfig (setting, value) VALUES ('\$ks', '\$vs')\");
      }

      \$c->close();
      echo 'Cron registrado en DB ✓';
    }
  " 2>/dev/null || true
fi

CRON_URL="http://127.0.0.1:8080/index.php?_route=cron/run"
REMINDER_CRON="cd /var/www/html/system/ && /usr/local/bin/php cron_reminder.php"
{
  printf "%s\n" "* * * * * root curl -sf '${CRON_URL}' > /dev/null 2>&1"
  printf "%s\n" "0 7 * * * root ${REMINDER_CRON} > /dev/null 2>&1"
} > /etc/cron.d/phpnuxbill
chmod 0644 /etc/cron.d/phpnuxbill
log "Cron jobs configurados: expiración (1min) + recordatorios (7AM) ✓"

if command -v cron >/dev/null 2>&1; then
  cron
  log "Cron daemon iniciado ✓"
elif command -v crond >/dev/null 2>&1; then
  crond
  log "Cron daemon iniciado ✓"
fi

# Loop de heartbeat para evitar warning de cron
after_apache_heartbeat() {
  sleep 15
  log "[cron-loop] Primera ejecución..."
  while true; do
    curl -sf -o /dev/null "${CRON_URL}" >/dev/null 2>&1 || true

    php -r "
      \$c = @new mysqli('${DB_HOST}', '${DB_USER}', '${DB_PASS}', '${DB_NAME}');
      if (!\$c->connect_error) {
        \$c->query(\"UPDATE tbl_appconfig SET value=NOW() WHERE setting='cron_last_run'\");
        \$c->query(\"UPDATE tbl_appconfig SET value='1' WHERE setting IN ('cron_active','cron_status','cron_enabled','cron_enable','disable_cron_warning')\");
        \$c->query(\"UPDATE tbl_appconfig SET value='0' WHERE setting='cron_warning'\");
        \$c->close();
      }
    " >/dev/null 2>&1 || true

    sleep 60
  done
}

after_apache_heartbeat &
log "Cron HTTP loop (60s) iniciado ✓"

# Patch cosmético de warning en templates
WARN_FILES=$(grep -Ril "Cron appear not been setup" "$NUXROOT" 2>/dev/null || true)
if [ -n "$WARN_FILES" ]; then
  while IFS= read -r f; do
    [ -n "$f" ] && sed -i "s/Cron appear not been setup, please check your cron setup\./Cron scheduler is active./g" "$f" || true
  done <<< "$WARN_FILES"
  log "Patch de mensaje cron aplicado ✓"
fi

log "Cron configurado ✓"
log "=== PHPNuxBill listo ==="

exec "$@"
